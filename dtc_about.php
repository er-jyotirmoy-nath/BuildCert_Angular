				<p style="text-autospace: none"><span lang="EN-US">Tempering valves can be either non-adjustable tempering valve with pre-set temperature (Type 1 valves) or adjustable with or without tool and with pre-set temperature (Type 2 valves).&nbsp; The standard EN15092 suggests that valves should be capable of distributing water at between 45 &#x2103; and 65 &#x2103; however the Water Supply (Water fittings) Regulations 1999 state that the hot water must be stored at a temperature of greater than 60 &#x2103; and distributed at a temperature of greater than<br>55 &#x2103;.</span>
