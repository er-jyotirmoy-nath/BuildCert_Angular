  <p>The <span lang="EN-US">Distribution Tempering valve Control Scheme (DTC Scheme) independently verifies that tempering valves comply with the minimum requirements of the Building Regulations part G and additionally ensures that the valves and the approval holders continue to meet these requirements throughout the valves approval period (5 years).&nbsp; The Scheme also requires additional information to be provided with the tempering valve to enable the valve to be installed and maintained to ensure correct operation. </span>
