 <p>EN15092 <span lang="EN-US">specifies dimensions, materials and performance requirements (including methods of test) for in line hot water supply tempering valves for sanitary hot water systems, of nominal sizes from DN 15 to DN 50. </span></p>
				
				<p style="text-autospace: none"><span lang="EN-US">Tempering valves reduce the temperature of sanitary hot water for distribution throughout the hot water system and are intended to be used with storage water heaters to provide tempered hot water to the terminal fitting. They are not intended to control the temperature at the point of use i.e. they are not safety devices, thermostatic mixing valves that are TMV2 approved must be used to control the temperature of water at the point of use.</span>
