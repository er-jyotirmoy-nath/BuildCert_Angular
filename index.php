      <h2>What is a BuildCert Approval?</h2>
      <h4>Who are BuildCert?</h4>
      <p>BuildCert is a  Product Testing and Certification program for plumbing products. Based in the UK, with over 75 years’ experience of water supply, we put our knowledge of plumbing systems to use for you, by examining, testing and certifying water fittings.
Our independent checks ensure that when you buy BuildCert Approved products, you can do so with peace of mind.</p>
 <h4>What checks are carried out?</h4>
      <p>BuildCert carries out a tests on representative samples of the products and then checks that manufacturers and suppliers have quality systems in place to ensure that every item is made to the same high quality.</p>
      <h4>UKAS Accreditation</h4>
      <p>BuildCert is an NSF program. The NSF laboratory in Wales is a UKAS Accredited organisation. UKAS Accreditation ensures that BuildCert certification is independent and trustworthy.
A full copy of our UKAS Schedule can be accessed on the UKAS website, or click here (pdf).</p>
