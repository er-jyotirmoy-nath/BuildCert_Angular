     <h2>Apply for BuildCert Approval</h2>
     <br><br>
 <p><strong>Why BuildCert? </strong> BuildCert is a product certification program. We provide independent, , third party, product certification of plumbing products to national and international standards and criteria.
Our goal is to provide certification services that meet your regulatory and marketing needs, while eliminating much of the bureaucracy and high cost that is often associated with product certification.</p>
      <p><strong>Opening Markets in the UK, Europe and the World </strong> BuildCert certification has worldwide recognition via NSF’s accredited lab for UKAS Accreditation to ISO/IEC 17065 (Conformity assessment “ Requirements for bodies certifying products, processes and services).
BuildCert can certify testing to UK Standards for plumbing products and to the requirements of the UK Building Regulations. BuildCert can also help you to access North American markets through NSF.</p>
 <p><strong>Proven Quality </strong> BuildCert enables you to differentiate your products from the competition.
Your BuildCert approval means that your products have been independently tested and approved as being fit for their intended use.
BuildCert certification gives your customers the reassurance that the products they are buying are of a good, consistent quality.
Reference to BuildCert certification in your sales material proves that the products you are selling are as good as you say they are.</p>
<p><strong>Bringing Users and Suppliers Closer Together </strong> BuildCert has developed a number of Schemes such as the TMV and CIA by bringing together manufacturing sectors and user groups to write specifications that set minimum standards that users can specify and manufacturers can provide. These schemes have ensured that products are fit for purpose and foster a trust between buyers and sellers.</p>
