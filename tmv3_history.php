  <h2 class=" bg-warning" style="padding:5px;border-color:#b8f9f7;">The History of the TMV3 Scheme</h2>
  <p>A meeting was held on the 10 January 1994 to which manufacturers of thermostatic mixing valves (TMV) were invited to discuss the testing of the above valves in order that they shall be safe for use in healthcare premises.</p><p>At a subsequent meeting, held on 10 February 1995 at the British Bathroom Council, Federation House, Stoke on Trent, a set of supply conditions was established which could affect the operation of the TMV and compromise the ability of the valve to safely control the temperature of the water.</p>
  <p>A Sub-Committee was appointed with a brief to produce a document to ensure that Thermostatic Mixing Valves for safe operation in Health Care Premises could be identified and certified.</p>
  <p>The document was drafted to include type testing for Third Party Certification and details of commissioning requirements and in-service testing.</p>
  <P>It was agreed that the membership of the Sub-Committee should comprise representatives from:-</P>
  <ul>
  <li>The manufacturing companies (drawn from a variety of sizes of company)</li>
  <li>The NHS Estates.</li>
  <li>A Test House.</li>
  </ul>
  <p>Engineers responsible for the purchase, installation, operation and maintenance of hospital hot water systems.</p>
  <p>An initial draft document to be called the National Health Service Model Engineering Specification, Thermostatic Mixing Valves (Health Care Premises), was presented to all manufacturers at two meetings held on 26/27 March and the 18 April 1996 where comments arising were discussed and voted upon and the final draft agreed.</p>
  <p>It was further agreed that the then WRc Evaluation and Testing Centre (WRc-NSF), now known as NSF International, should, on behalf of NHS Estates and the Thermostatic Mixing Valve manufacturers, operate a Third Party Certification Scheme for the products concerned and handle all NHS estates correspondence on Thermostatic Mixing Valves. The requirements for Type 3 valves to be Third Party Certificated was incorporated in the 1998 Health Guidance Notes revision of Safe Hot Water and Surface Temperatures.</p>
